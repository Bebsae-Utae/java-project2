package com.example.javaproject2.week2.d5;

public class ElseEx {
    public static void main(String[] args) {
        boolean checkTrue = true;
        if(checkTrue){
            System.out.println("true임");
        } else {
            System.out.println("false임");
        }

        boolean checkFalse = false;
        if (checkFalse) {
            System.out.println("true임");
        } else {
            System.out.println("false임");
        }

    }
}
